// Introdução a árvores Trie

// Quando falamos de árvore Trie, precisamos saber que a pronúncia correta é “trai” e que é também conhecida como 
//árvore de prefixos. Ela é uma estrutura de dados especializada no armazenamento e manipulação de strings, com 
// excelente desempenho para buscas rápidas, autocompletar e verificação de palavras. Ao contrário das árvores BST, 
// que organizam os dados de acordo com valores numéricos ou ordem alfabética total, a Trie organiza cada 
// palavra letra por letra, aproveitando ao máximo os prefixos comuns entre elas.

// Essa estrutura é bastante utilizada em dicionários digitais, sistemas de correção automática, algoritmos de 
// compressão e qualquer aplicação que envolva grande quantidade de palavras ou buscas frequentes com base em prefixos.